#include "../../libft.h"

/// @brief Allocates a new node for a linked list with the given content.
/// @param content The content to be stored in the new node.
/// @return The new node created or NULL if the allocation fails.
t_list	*ft_lstnew(void *content)
{
	t_list	*new;

	new = (t_list *)malloc(sizeof(t_list));
	if (!new)
		return (0);
	new->content = content;
	new->next = NULL;
	return (new);
}
