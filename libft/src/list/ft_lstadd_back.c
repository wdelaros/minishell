#include "../../libft.h"

/// @brief Add the new element at the end of the linked list.
/// @param lst A pointer to the pointer to the first element of the list.
/// @param new The element to be added at the end of the list.
void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*last;

	if (lst)
	{
		if (*lst)
		{
			last = ft_lstlast(*lst);
			last->next = new;
		}
		else
			*lst++ = new;
	}
}
