#include "../../libft.h"

/// @brief Applies a given function to each character in a string, providing 
/// its index as first argument.
/// @param s The string to iterate through.
/// @param f The function to apply to each character of the string.
void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	unsigned int	i;

	i = 0;
	if (!s)
		return ;
	while (s[i])
	{
		f(i, &s[i]);
		i++;
	}
}
